#include "../../Header/Minishell.h"

int	is_a_legit_va_env(char *s)
{
	if (!s || (s[0] >= '0' && s[0] <= '9') || type_of_str(s) >= 0)
	{
		if (s && s[0] == '$')
			return (TRUE);
		return (FALSE);
	}
	return (TRUE);
}
